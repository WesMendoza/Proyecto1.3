     // Método para agregar los datos a la tabla
    private void agregarDatosATabla() {
        
        // Limpiar la tabla antes de agregar nuevos datos
        modeloTabla.setRowCount(0);
        if (listaEstudiantes != null) {
        // Recorrer la lista y agregar cada objeto como fila en la tabla
        for (DatosEstudiantes estudiante : listaEstudiantes) {
            Object[] fila = {
                estudiante.getCedula(),
                estudiante.getApellido(),
                estudiante.getNombre(),
                estudiante.getmateria(),
                estudiante.getPromedio(), // Suponiendo que "Promedio" representa la "Nota Final"
                estudiante.getEstado()
            };
            modeloTabla.addRow(fila);
        }
    }
    }
