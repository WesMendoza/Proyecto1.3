    private void bttDescargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttDescargarActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        int option = fileChooser.showSaveDialog(this);
        if (option == JFileChooser.APPROVE_OPTION) {
            try (FileWriter writer = new FileWriter(fileChooser.getSelectedFile() + ".csv")) {
                // Escribir el encabezado
                writer.write("Cédula,Nombres,Apellidos,Materia,Promedio Final,Estado\n");

                // Escribir los datos de los estudiantes
                for (DatosEstudiantes estudiante : listaEstudiantes) {
                    writer.write(estudiante.getCedula() + ","
                            + estudiante.getNombre() + ","
                            + estudiante.getApellido() + ","
                            + estudiante.getmateria() + ","
                            + estudiante.getPromedio() + ","
                            + estudiante.getEstado() + "\n");
                }

                JOptionPane.showMessageDialog(this, "Datos descargados correctamente", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            } catch (IOException e) {
                JOptionPane.showMessageDialog(this, "Error al guardar el archivo", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_bttDescargarActionPerformed

    

